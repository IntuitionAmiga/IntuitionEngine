func fpuPool(labels ...string) []string {
	pool := map[string]string{
		"zero":      ".fp_zero:\n                dc.l    $00000000,$00000000",
		"one":       ".fp_one:\n                dc.l    $3FF00000,$00000000",
		"2":         ".fp_2:\n                dc.l    $40000000,$00000000",
		"3":         ".fp_3:\n                dc.l    $40080000,$00000000",
		"3_7":       ".fp_3_7:\n                dc.l    $400D9999,$9999999A",
		"neg_3_7":   ".fp_neg_3_7:\n                dc.l    $C00D9999,$9999999A",
		"4":         ".fp_4:\n                dc.l    $40100000,$00000000",
		"5":         ".fp_5:\n                dc.l    $40140000,$00000000",
		"6":         ".fp_6:\n                dc.l    $40180000,$00000000",
		"8":         ".fp_8:\n                dc.l    $40200000,$00000000",
		"10":        ".fp_10:\n                dc.l    $40240000,$00000000",
		"12":        ".fp_12:\n                dc.l    $40280000,$00000000",
		"16":        ".fp_16:\n                dc.l    $40300000,$00000000",
		"20":        ".fp_20:\n                dc.l    $40340000,$00000000",
		"42":        ".fp_42:\n                dc.l    $40450000,$00000000",
		"42_5":      ".fp_42_5:\n                dc.l    $40454000,$00000000",
		"100":       ".fp_100:\n                dc.l    $40590000,$00000000",
		"neg_42":    ".fp_neg_42:\n                dc.l    $C0450000,$00000000",
		"pi_over_2": ".fp_pi_over_2:\n                dc.l    $3FF921FB,$54442D18",
		"inf":       ".fp_inf:\n                dc.l    $7FF00000,$00000000",
		"nan":       ".fp_nan:\n                dc.l    $7FF80000,$00000000",
		"e":         ".fp_e:\n                dc.l    $4005BF0A,$8B145769",
		"a_1_5":     ".fp_a:\n                dc.l    $3FF80000,$00000000",
		"b_2_5":     ".fp_b:\n                dc.l    $40040000,$00000000",
		"3_5":       ".fp_3_5:\n                dc.l    $400C0000,$00000000",
		"neg_2_5":   ".fp_neg_2_5:\n                dc.l    $C0040000,$00000000",
		"neg_1":     ".fp_neg_1:\n                dc.l    $BFF00000,$00000000",
		"half":      ".fp_half:\n                dc.l    $3FE00000,$00000000",
	}
	var out []string
	for _, l := range labels {
		if v, ok := pool[l]; ok {
			out = append(out, v)
		}
	}
	return out
}

