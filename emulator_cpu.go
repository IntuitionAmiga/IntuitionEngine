package main

import "sync"

// EmulatorCPU is the unified CPU interface for all processor modes.
type EmulatorCPU interface {
	LoadProgram(filename string) error
	Reset()
	Execute()
	Stop()
	StartExecution()
}

// activeCPU is the currently running CPU instance, used by the video backend
// for window-close cleanup.
var activeCPU EmulatorCPU

// activeVideoChip is the currently active video chip, used by cpu_ie32.go for
// direct VRAM access during Reset().
var activeVideoChip *VideoChip

// resetMu serializes F10 handler, IPC OPEN handler, and program state updates.
var resetMu sync.Mutex
