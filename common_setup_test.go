package main

import (
	"log"
	"os"
	"testing"
	"time"
)

var (
	chip      *SoundChip
	videoChip *VideoChip
)

func TestMain(m *testing.M) {
	var err error

	// Initialize sound
	chip, err = NewSoundChip(AUDIO_BACKEND_OTO)
	if err != nil {
		log.Fatalf("Failed to create global sound chip: %v", err)
	}

	// Initialize video
	videoChip, err = NewVideoChip(VIDEO_BACKEND_EBITEN)
	if err != nil {
		log.Fatalf("Failed to create global video chip: %v", err)
	}

	// Start chips
	chip.Start()
	videoChip.HandleWrite(VIDEO_CTRL, 1)
	videoChip.Start()

	// Allow backends to initialize
	time.Sleep(100 * time.Millisecond)

	// Run tests
	code := m.Run()

	// Cleanup
	videoChip.Stop()
	chip.Stop()

	os.Exit(code)
}
