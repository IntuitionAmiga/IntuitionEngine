/*
antic_constants.go - ANTIC Video Chip Constants

The ANTIC (Alphanumeric Television Interface Controller) is the custom video
chip from Atari 8-bit computers (400/800/XL/XE series). It provides:

- Display list-driven graphics (14 text and graphics modes)
- 320x192 active display area (384x240 with borders)
- 128 colors (16 hues × 8 luminances)
- Fine scrolling (horizontal and vertical)
- Player-missile graphics (hardware sprites)
- DMA-based memory fetch
- WSYNC for precise raster synchronization

Register Map (IE32/M68K/x86):
- Base address: 0xF2100 (between ULA at 0xF2000 and next available)
- All registers are 4-byte aligned for copper coprocessor compatibility

Register Map (6502):
- Authentic Atari addresses: 0xD400-0xD40F
- Compatible with original Atari software
*/

package main

// =============================================================================
// ANTIC Register Addresses (IE32/M68K/x86 direct access)
// =============================================================================

const (
	// ANTIC register base address
	ANTIC_BASE = 0xF2100

	// ANTIC_DMACTL - DMA control register
	// Bit 5: Enable display list DMA
	// Bits 1-0: Playfield width (00=off, 01=narrow, 10=normal, 11=wide)
	// Bit 2: Enable player DMA
	// Bit 3: Enable missile DMA
	// Bit 4: Player resolution (0=double, 1=single)
	ANTIC_DMACTL = 0xF2100

	// ANTIC_CHACTL - Character control register
	// Bit 0: Video inverse (blank instead of inverse)
	// Bit 1: Video reflect (vertical mirror)
	// Bit 2: Blank (force blank display)
	ANTIC_CHACTL = 0xF2104

	// ANTIC_DLISTL - Display list pointer low byte
	ANTIC_DLISTL = 0xF2108

	// ANTIC_DLISTH - Display list pointer high byte
	ANTIC_DLISTH = 0xF210C

	// ANTIC_HSCROL - Horizontal fine scroll (0-15)
	ANTIC_HSCROL = 0xF2110

	// ANTIC_VSCROL - Vertical fine scroll (0-15)
	ANTIC_VSCROL = 0xF2114

	// ANTIC_PMBASE - Player-missile base address (high byte)
	ANTIC_PMBASE = 0xF2118

	// ANTIC_CHBASE - Character set base address (high byte)
	ANTIC_CHBASE = 0xF211C

	// ANTIC_WSYNC - Wait for horizontal sync (write only)
	ANTIC_WSYNC = 0xF2120

	// ANTIC_VCOUNT - Vertical line counter (read only, returns scanline/2)
	ANTIC_VCOUNT = 0xF2124

	// ANTIC_PENH - Light pen horizontal position (read only)
	ANTIC_PENH = 0xF2128

	// ANTIC_PENV - Light pen vertical position (read only)
	ANTIC_PENV = 0xF212C

	// ANTIC_NMIEN - NMI enable register
	// Bit 6: DLI enable
	// Bit 7: VBI enable
	ANTIC_NMIEN = 0xF2130

	// ANTIC_NMIST - NMI status (read) / NMIRES (write to reset)
	// Bit 5: Reset button status
	// Bit 6: DLI pending
	// Bit 7: VBI pending
	ANTIC_NMIST = 0xF2134

	// ANTIC_ENABLE - Video enable (IE-specific extension)
	// Bit 0: Enable ANTIC video output
	ANTIC_ENABLE = 0xF2138

	// ANTIC_STATUS - Status register (IE-specific extension)
	// Bit 0: VBlank active
	ANTIC_STATUS = 0xF213C

	// ANTIC register region end
	ANTIC_END = 0xF213F
)

// =============================================================================
// ANTIC Register Bit Masks
// =============================================================================

// DMACTL bits
const (
	ANTIC_DMA_NARROW  = 0x01 // Narrow playfield (128 color clocks)
	ANTIC_DMA_NORMAL  = 0x02 // Normal playfield (160 color clocks)
	ANTIC_DMA_WIDE    = 0x03 // Wide playfield (192 color clocks)
	ANTIC_DMA_MISSILE = 0x04 // Enable missile DMA
	ANTIC_DMA_PLAYER  = 0x08 // Enable player DMA
	ANTIC_DMA_PMRES   = 0x10 // Player/missile resolution (1=single line)
	ANTIC_DMA_DL      = 0x20 // Enable display list DMA
)

// CHACTL bits
const (
	ANTIC_CHACTL_BLANK   = 0x01 // Blank instead of inverse video
	ANTIC_CHACTL_INVERT  = 0x02 // Invert video (use background color)
	ANTIC_CHACTL_REFLECT = 0x04 // Reflect characters vertically
)

// NMIEN bits
const (
	ANTIC_NMIEN_DLI = 0x80 // Display List Interrupt enable
	ANTIC_NMIEN_VBI = 0x40 // Vertical Blank Interrupt enable
)

// NMIST bits (read)
const (
	ANTIC_NMIST_RESET = 0x20 // Reset button pressed
	ANTIC_NMIST_DLI   = 0x80 // DLI pending
	ANTIC_NMIST_VBI   = 0x40 // VBI pending
)

// Enable register bits
const (
	ANTIC_ENABLE_VIDEO = 0x01 // Bit 0: Video enable
)

// Status register bits
const (
	ANTIC_STATUS_VBLANK = 0x01 // Bit 0: VBlank active
)

// =============================================================================
// ANTIC Display Dimensions
// =============================================================================

const (
	// Active display area
	ANTIC_DISPLAY_WIDTH  = 320
	ANTIC_DISPLAY_HEIGHT = 192

	// Border size (NTSC)
	ANTIC_BORDER_LEFT   = 32
	ANTIC_BORDER_RIGHT  = 32
	ANTIC_BORDER_TOP    = 24
	ANTIC_BORDER_BOTTOM = 24

	// Total frame dimensions (display + borders)
	ANTIC_FRAME_WIDTH  = ANTIC_DISPLAY_WIDTH + ANTIC_BORDER_LEFT + ANTIC_BORDER_RIGHT  // 384
	ANTIC_FRAME_HEIGHT = ANTIC_DISPLAY_HEIGHT + ANTIC_BORDER_TOP + ANTIC_BORDER_BOTTOM // 240

	// Scanlines
	ANTIC_SCANLINES_NTSC = 262
	ANTIC_SCANLINES_PAL  = 312
)

// =============================================================================
// ANTIC Timing Constants
// =============================================================================

const (
	// Compositor layer (between TED=12 and ULA=15)
	ANTIC_LAYER = 13
)

