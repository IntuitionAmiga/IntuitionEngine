// lh1_decompress.go - Pure Go LH1 (LZHUF) decompressor.
// Original algorithm by Haruyasu Yoshizaki (1988).
//
// LH1 uses a 4096-byte sliding window with adaptive Huffman coding
// for 314 symbols (256 literals + 58 length codes) and a static
// Huffman code for 12-bit match positions.

package main

import "fmt"

const (
	lh1N         = 4096                      // Sliding window size
	lh1F         = 60                        // Maximum match length
	lh1Threshold = 2                         // Encode length = actual - (THRESHOLD+1)
	lh1NChar     = 256 - lh1Threshold + lh1F // 314 symbols
	lh1T         = lh1NChar*2 - 1            // 627 total tree nodes
	lh1R         = lh1T - 1                  // 626 root node index
	lh1MaxFreq   = 0x8000                    // Trigger tree reconstruction
)

