type lh1Decoder struct {
	src    []byte
	srcPos int
	err    error
	padded bool

	// Bit buffer (MSB-aligned, 16-bit register like original LZHUF)
	getBuf uint16
	getLen uint8

	freq [lh1T + 1]uint32
	prnt [lh1T + lh1NChar]int32
	son  [lh1T]int32
}

func decompressLH1(src []byte, origSize int) ([]byte, error) {
	if origSize <= 0 {
		return nil, fmt.Errorf("lh1: invalid original size %d", origSize)
	}
	if len(src) == 0 {
		return nil, fmt.Errorf("lh1: empty compressed data")
	}

	d := &lh1Decoder{src: src}
	d.startHuff()

	out := make([]byte, origSize)
	var textBuf [lh1N]byte
	// Initialize window with spaces (matches original LZHUF)
	for i := range lh1N - lh1F {
		textBuf[i] = 0x20
	}
	r := lh1N - lh1F
	outPos := 0

	for outPos < origSize {
		c, err := d.decodeChar()
		if err != nil {
			return nil, err
		}
		if c < 256 {
			out[outPos] = byte(c)
			textBuf[r] = byte(c)
			r = (r + 1) & (lh1N - 1)
			outPos++
		} else {
			matchLen := c - 255 + lh1Threshold // length 3..60
			if matchLen > origSize-outPos {
				matchLen = origSize - outPos
			}
			pos, err := d.decodePosition()
			if err != nil {
				return nil, err
			}
			i := (r - pos - 1) & (lh1N - 1)
			for k := range matchLen {
				b := textBuf[(i+k)&(lh1N-1)]
				out[outPos] = b
				textBuf[r] = b
				r = (r + 1) & (lh1N - 1)
				outPos++
				if outPos >= origSize {
					break
				}
			}
		}
	}

	return out, nil
}

func (d *lh1Decoder) startHuff() {
	// Initialize leaf nodes with frequency 1
	for i := range lh1NChar {
		d.freq[i] = 1
		d.son[i] = int32(i + lh1T)
		d.prnt[i+lh1T] = int32(i)
	}

	// Build internal nodes by pairing leaves bottom-up
	i := 0
	j := lh1NChar
	for j <= lh1R {
		d.freq[j] = d.freq[i] + d.freq[i+1]
		d.son[j] = int32(i)
		d.prnt[i] = int32(j)
		d.prnt[i+1] = int32(j)
		i += 2
		j++
	}

	d.freq[lh1T] = 0xFFFF // sentinel
	d.prnt[lh1R] = 0      // root has no parent
}

func (d *lh1Decoder) getBit() (int, error) {
	for d.getLen <= 8 {
		var b byte
		if d.srcPos < len(d.src) {
			b = d.src[d.srcPos]
			d.srcPos++
		} else {
			if !d.padded && d.getLen > 0 {
				d.padded = true
				b = 0
			} else {
				d.err = fmt.Errorf("lh1: unexpected EOF")
				return 0, d.err
			}
		}
		d.getBuf |= uint16(b) << (8 - d.getLen)
		d.getLen += 8
	}
	val := d.getBuf
	d.getBuf <<= 1
	d.getLen--
	if val&0x8000 != 0 {
		return 1, nil
	}
	return 0, nil
}

func (d *lh1Decoder) getByte() (int, error) {
	for d.getLen <= 8 {
		var b byte
		if d.srcPos < len(d.src) {
			b = d.src[d.srcPos]
			d.srcPos++
		} else {
			if !d.padded && d.getLen > 0 {
				d.padded = true
				b = 0
			} else {
				d.err = fmt.Errorf("lh1: unexpected EOF")
				return 0, d.err
			}
		}
		d.getBuf |= uint16(b) << (8 - d.getLen)
		d.getLen += 8
	}
	val := d.getBuf
	d.getBuf <<= 8
	d.getLen -= 8
	return int(val >> 8), nil
}

func (d *lh1Decoder) decodeChar() (int, error) {
	c := d.son[lh1R]
	for c < lh1T {
		bit, err := d.getBit()
		if err != nil {
			return 0, err
		}
		c += int32(bit)
		if c < 0 || int(c) >= len(d.son) {
			return 0, fmt.Errorf("lh1: corrupt huffman tree")
		}
		c = d.son[c]
	}
	c -= lh1T
	if c < 0 || c >= lh1NChar {
		return 0, fmt.Errorf("lh1: invalid character code %d", c)
	}
	d.update(int(c))
	if d.err != nil {
		return 0, d.err
	}
	return int(c), nil
}

func (d *lh1Decoder) decodePosition() (int, error) {
	i, err := d.getByte()
	if err != nil {
		return 0, err
	}
	c := int(lh1DCode[i]) << 6
	j := int(lh1DLen[i]) - 2
	for j > 0 {
		bit, err := d.getBit()
		if err != nil {
			return 0, err
		}
		i = (i << 1) | bit
		j--
	}
	return c | (i & 0x3F), nil
}

func (d *lh1Decoder) reconst() {
	// Phase 1: collect leaves and halve their frequencies
	j := 0
	for i := range lh1T {
		if d.son[i] >= lh1T {
			d.freq[j] = (d.freq[i] + 1) / 2
			d.son[j] = d.son[i]
			j++
		}
	}

	// Phase 2: rebuild internal nodes bottom-up
	i := 0
	for jj := lh1NChar; jj < lh1T; jj++ {
		f := d.freq[i] + d.freq[i+1]
		d.freq[jj] = f
		// Insert in sorted position
		k := jj - 1
		for k > 0 && f < d.freq[k] {
			k--
		}
		k++
		// Shift freq and son arrays right by 1 from k to jj-1
		copy(d.freq[k+1:jj+1], d.freq[k:jj])
		d.freq[k] = f
		copy(d.son[k+1:jj+1], d.son[k:jj])
		d.son[k] = int32(i)
		i += 2
	}

	// Phase 3: reconnect parent pointers
	for i := range lh1T {
		k := d.son[i]
		if k >= lh1T {
			d.prnt[k] = int32(i)
		} else {
			d.prnt[k] = int32(i)
			d.prnt[k+1] = int32(i)
		}
	}
}

func (d *lh1Decoder) update(c int) {
	if d.freq[lh1R] == lh1MaxFreq {
		d.reconst()
	}

	c = int(d.prnt[c+lh1T])

	for c != 0 {
		d.freq[c]++
		k := d.freq[c]

		// Check if frequency order is violated
		l := c + 1
		if l < len(d.freq) && k > d.freq[l] {
			// Find rightmost node with frequency < k
			for l+1 < len(d.freq) && k > d.freq[l+1] {
				l++
			}
			// Now freq[l] < k <= freq[l+1] — but we want the last one < k
			// Actually: we incremented past all freq < k, so l is the swap target

			// Swap frequencies
			if l >= len(d.son) {
				d.err = fmt.Errorf("lh1: corrupt frequency tree")
				return
			}
			d.freq[c] = d.freq[l]
			d.freq[l] = k

			// Swap children and update parent pointers
			i := d.son[c]
			if i < 0 || int(i) >= len(d.prnt) {
				d.err = fmt.Errorf("lh1: corrupt parent pointer")
				return
			}
			d.prnt[i] = int32(l)
			if i < lh1T && int(i)+1 < len(d.prnt) {
				d.prnt[i+1] = int32(l)
			}

			jj := d.son[l]
			d.son[l] = i

			if jj < 0 || int(jj) >= len(d.prnt) {
				d.err = fmt.Errorf("lh1: corrupt parent pointer")
				return
			}
			d.prnt[jj] = int32(c)
			if jj < lh1T && int(jj)+1 < len(d.prnt) {
				d.prnt[jj+1] = int32(c)
			}
			d.son[c] = jj

			c = l
		}

		c = int(d.prnt[c])
	}
}
